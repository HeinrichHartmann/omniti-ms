# Load support functions
. ../../lib/functions.sh

PROG=pbzip2
VER=1.1.8
PKG=omniti/compress/pbzip2
SUMMARY="A parallel implementation of the bzip2 file compressor"
DESC="$SUMMARY"

# 64-bit only
BUILDARCH=64

LDFLAGS64="$LDFLAGS64 -L/opt/omni/lib/$ISAPART64 -R/opt/omni/lib/$ISAPART64"
CFLAGS="-fast -D_LARGEFILE64_SOURCE -D_FILE_OFFSET_BITS=64 -KPIC"

# There is no configure here, so just export vars
configure64() {
    export PREFIX CFLAGS="$CFLAGS $CFLAGS64" \
    CXXFLAGS="$CXXFLAGS $CXXFLAGS64" \
    CPPFLAGS="$CPPFLAGS $CPPFLAGS64" \
    LDFLAGS="$LDFLAGS $LDFLAGS64" \
    CC=$CC CXX=$CXX
}

init
download_source $PROG $PROG $VER
patch_source
prep_build
build
make_isa_stub
make_package
clean_up

